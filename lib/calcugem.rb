# Basic calculator ruby gem

class Calcugem
  def sum(a, b)
    a + b
  end

  def subtract(a, b)
    a - b
  end

  def multiply(a, b)
    a * b
  end

  def divide(a, b)
    a / b
  end

  def exponent (a, b)
    a ** b
  end

  def square_root(a)
    Math.sqrt(a)
  end

  def cube_root(a)
    Math.cbrt(a)
  end
end
